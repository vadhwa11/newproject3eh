package jkt.hms.util;

import java.math.BigDecimal;
import java.util.Date;

public class ChildPojoForMasstoreItem {
	private int id;
	private Date issueDate;
	private int itemId;
	private String description;
	private String departmentName;
	private String patientName;
	private String employeeName;
	private BigDecimal qnt;
	private int issueOrReciptType;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Date getIssueDate() {
		return issueDate;
	}

	public void setIssueDate(Date issueDate) {
		this.issueDate = issueDate;
	}

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public String getPatientName() {
		return patientName;
	}

	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public BigDecimal getQnt() {
		return qnt;
	}

	public void setQnt(BigDecimal qnt) {
		this.qnt = qnt;
	}

	public int getIssueOrReciptType() {
		return issueOrReciptType;
	}

	public void setIssueOrReciptType(int issueOrReciptType) {
		this.issueOrReciptType = issueOrReciptType;
	}

}
