package jkt.hms.util;

import java.util.Comparator;

import jkt.hms.masters.business.base.BaseMasDepartment;

public class ComaparatorForBaseMasDepartment  implements Comparator<BaseMasDepartment> {

    public int compare(BaseMasDepartment obj1, BaseMasDepartment obj2) {
        return obj1.getDepartmentName().compareTo(obj2.getDepartmentName());
    }
}  
