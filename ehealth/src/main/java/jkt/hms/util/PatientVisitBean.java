package jkt.hms.util;

public class PatientVisitBean {
	
	private int hospitalId;
	private int departmentId;
	private int tokenNo;
	private int currentPriority;
	private int uhinNo;
	private long totalHospitalVisitNo;
	
	public PatientVisitBean(int hospitalId,int departmentId,int tokenNo,int currentPriority,int uhinNo,long totalHospitalVisitNo){
		this.hospitalId=hospitalId;
		this.departmentId=departmentId;
		this.tokenNo=tokenNo;
		this.currentPriority=currentPriority;
		this.uhinNo=uhinNo;
		this.setTotalHospitalVisitNo(totalHospitalVisitNo);
		
	}
	
	public int getHospitalId() {
		return hospitalId;
	}
	public void setHospitalId(int hospitalId) {
		this.hospitalId = hospitalId;
	}
	public int getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}
	public int getTokenNo() {
		return tokenNo;
	}
	public void setTokenNo(int tokenNo) {
		this.tokenNo = tokenNo;
	}
	public int getCurrentPriority() {
		return currentPriority;
	}
	public void setCurrentPriority(int currentPriority) {
		this.currentPriority = currentPriority;
	}
	public int getUhinNo() {
		return uhinNo;
	}
	public void setUhinNo(int uhinNo) {
		this.uhinNo = uhinNo;
	}

	public long getTotalHospitalVisitNo() {
		return totalHospitalVisitNo;
	}

	public void setTotalHospitalVisitNo(long totalHospitalVisitNo) {
		this.totalHospitalVisitNo = totalHospitalVisitNo;
	}
	

}
